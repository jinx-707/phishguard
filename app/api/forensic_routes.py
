"""
Forensic Scan API Routes - PART 5 UI Contract

This module provides the forensic analysis endpoint that:
1. Receives structured forensic signals from Chrome extension
2. Enriches with domain intelligence
3. Generates structured FINDINGS
4. Computes deterministic risk scores
5. Returns dynamic explanations

Response format:
{
  "risk": "HIGH | MEDIUM | LOW",
  "confidence": float,
  "summary": string,
  "findings": [...],
  "advanced": {...}
}
"""

from fastapi import APIRouter, HTTPException, status
from pydantic import BaseModel
from typing import Optional, List, Dict, Any
import structlog

from app.services.forensic_engine import get_forensic_engine
from app.services.threat_graph_engine import get_threat_engine

logger = structlog.get_logger(__name__)

router = APIRouter(prefix="/scan", tags=["forensic"])


class ForensicScanRequest(BaseModel):
    """Request schema for forensic signal analysis."""
    url_context: Dict[str, str]
    form_analysis: Dict[str, Any]
    script_analysis: Dict[str, Any]
    dom_manipulation: Dict[str, Any]
    content_analysis: Dict[str, Any]
    timestamp: Optional[int] = None
    extension_version: Optional[str] = "2.0.0"


class ForensicScanResponse(BaseModel):
    """Response schema for forensic analysis - PART 5 UI Contract."""
    risk: str  # HIGH, MEDIUM, LOW
    confidence: float
    summary: str
    findings: List[Dict[str, Any]]
    advanced: Dict[str, Any]


@router.post("/forensic", response_model=ForensicScanResponse)
async def scan_forensic(request: ForensicScanRequest):
    """
    Forensic analysis endpoint - PART 2, 3, 4, 5.
    
    Receives structured forensic signals from Chrome extension.
    Returns:
    - risk: HIGH | MEDIUM | LOW
    - confidence: float
    - summary: Dynamic human-readable paragraph
    - findings: Array of structured findings
    - advanced: Technical breakdown with real values
    """
    import time
    start_time = time.time()
    
    # Extract domain from URL context
    domain = request.url_context.get("current_domain", "")
    
    logger.info("Forensic scan started", domain=domain)
    
    # Get threat engine for enrichment
    threat_engine = None
    try:
        threat_engine = await get_threat_engine()
    except Exception as e:
        logger.warning(f"Threat engine not available: {e}")
    
    # Get forensic engine
    forensic_engine = get_forensic_engine(threat_engine)
    
    # Build signals dict from request
    signals = {
        "url_context": request.url_context,
        "form_analysis": request.form_analysis,
        "script_analysis": request.script_analysis,
        "dom_manipulation": request.dom_manipulation,
        "content_analysis": request.content_analysis,
    }
    
    # Run forensic analysis
    try:
        result = await forensic_engine.analyze(signals, domain)
        
        elapsed = (time.time() - start_time) * 1000
        logger.info("Forensic scan completed", 
                   domain=domain,
                   risk=result.get("risk"),
                   elapsed_ms=round(elapsed, 2))
        
        return ForensicScanResponse(**result)
        
    except Exception as e:
        logger.error("Forensic analysis failed", error=str(e))
        raise HTTPException(
            status_code=status.HTTP_500_INTERNAL_SERVER_ERROR,
            detail=f"Forensic analysis failed: {str(e)}"
        )

